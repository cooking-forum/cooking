function checkRicetta() {
    if (document.myRicetta.nomeRicetta.value == "") {
        window.alert("Inserire nome ricetta!");
    }

    if (document.myRicetta.nomeAutore.value == "") {
        window.alert("Inserire nome autore!");
    }

    if (document.myRicetta.ingredienti.value == "") {
        window.alert("Inserire ingredienti!");
    }

    if (document.myRicetta.nomeRicetta.value == "") {
        window.alert("Inserire descrizione del procedimento!");
    }
}